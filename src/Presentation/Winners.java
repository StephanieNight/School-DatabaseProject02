package Presentation;

/**
 *
 * @author Stephanie
 */
public class Winners {
    private String name;
    private String team;
    private String wins;

    public Winners(String name, String team, String wins) {
        this.name = name;
        this.team = team;
        this.wins = wins;
    }

    public String getName() {
        return name;
    }

    public String getTeam() {
        return team;
    }

    public String getWins() {
        return wins;
    }
    
            
    
}
